    <h3 class="card-title">Plank to Dolphin</h3>
    <p class="card-text">"Plank to Dolphin is a great warm-up for the shoulders, arms, and core," says West. "We want the shoulders warm for the push-ups to come." (Don't say you weren't warned!)<br>
  <ul>
 <li>Start in a forearm plank with your forearms on the floor, elbows directly underneath your shoulders, hands facing forward so that your arms are parallel, and legs extended behind you. Tuck your tailbone and engage your core, butt, and quads. This is the starting position.</li>
  <li>Press through your forearms and lift your hips up and back, creating an inverted V shape with your body. Your head should now be between your shoulders.</li>
  <li>Pause for a second and then slowly lower back into a forearm plank.</li>
  <li>Continue this movement for for 1 minute.</li>
    <li><i>Targets the core, deltoids, rhomboids, and quads.</i></li>
</ul> </p>
