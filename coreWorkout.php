<h1 class="mt-5 text-center">Stretching workouts</h1>
<h5 class="mx-5">Directions:</h5>
<p class="mx-5">Perform each exercise for one minute, resting 30 seconds after each move.
<br>
As you get comfortable, you can swap that 30 seconds of rest for jogging in place, suggests West. Keep track of your reps, and try to squeeze in more every time you do the workout, she adds.<br>

If you’re feeling up for it, you can do this workout twice for a total 30-minute workout.<br></p>

