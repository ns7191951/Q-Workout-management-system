  <div class="card-body">
    <h3 class="card-title">Push-Up</h3>
    <p class="card-text">Push-ups work your abs, arms, and shoulders all at once, says West—win, win, win.<br>
  <ul>
 <li>Start in a high plank with your palms flat on the floor, hands shoulder-width apart, shoulders stacked directly above your wrists, legs extended behind you, and your core and glutes engaged.</li>
  <li>Bend your elbows and lower your body to the floor. Drop to your knees if needed.</li>
  <li>Push through the palms of your hands to straighten your arms. </li>
  <li>Continue this movement for for 1 minute.</li>
    <li><i>Targets the core, pectorals, deltoids, and triceps.</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/core/planktap.jpg" class="card-img-top text-center" style="width: 36rem;" alt="planktap">
  <div class="card-body">
    <h3 class="card-title"> Plank Tap</h3>
    <p class="card-text">"Adding shoulder taps to a plank increases the [core] work needed to maintain stability," says West. It also activates your shoulders, which take turns bearing weight.<br>
  <ul>
 <li>Start in a high plank position with your palms flat on the floor, hands shoulder-width apart, shoulders stacked directly above your wrists, legs extended behind you, and your core and glutes engaged.</li>
  <li>Tap your right hand to your left shoulder while engaging your core and glutes to keep your hips as still as possible so that they're not swaying from side to side.</li>
  <li>Do the same thing with your left hand to right shoulder.</li>
  <li>Continue alternating sides for 1 minute.</li>
  <li>To make this easier, try separating your legs a little more.</li>
    <li><i>Targets the core, deltoids, and triceps.</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/core/sideplank.jpg" class="card-img-top text-center" style="width: 36rem;" alt="sideplank">
  <div class="card-body">
    <h3 class="card-title"> Forearm Side Plank With Twist</h3>
    <p class="card-text">"No core workout would be complete without side work," says West. A side plank alone is challenging, and the twist adds a little extra oomph.<br>
  <ul>
 <li>Start in a forearm side plank by propping your body up on your left forearm, with your elbow stacked underneath your shoulder and your hand in front of your body. Extend your legs and stack your right foot on top of your left, and then squeeze your abs and glutes to lift your hips off the floor.</li>
  <li>Place your right arm behind your head, with your elbow bent and pointing up toward the ceiling. This is the starting position.</li>
  <li>Rotate your torso toward the floor, bringing your right elbow to meet your left hand. Don't let your hips drop—the movement should just come from your core.</li>
  <li>Then, reverse the movement to return to the starting position.</li>
  <li>Continue for 1 minute. Then repeat for 1 minute on the opposite side.</li>
  <li><i>Targets the the core (especially the obliques) and the deltoids.</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/core/bicycle.jpg" class="card-img-top text-center" style="width: 36rem;" alt="bicycle">
  <div class="card-body">
    <h3 class="card-title">Bicycle Crunch</h3>
    <p class="card-text">"Bicycle crunches let the arms rest but create enormous work in the midsection by moving the upper body and lower body simultaneously," says West. "The emphasis is on bringing the opposite elbow and knee together while still maintaining a flat back on the floor." <br>
  <ul>
 <li>Lie faceup with your legs in tabletop position (knees bent 90 degrees and stacked over your hips). Place your hands behind your head, elbows bent and pointing out to the sides. Use your abs to curl your shoulders off the floor. This is the starting position.</li>
  <li>Twist to bring your right elbow to your left knee, while simultaneously straightening your right leg.</li>
  <li>Then, twist to bring your left elbow to your right knee, simultaneously straightening your left leg.</li>
  <li>Continue alternating sides for 1 minute. Go at a slow and steady pace so that you can really twist and feel your abs working.</li>
  <li><i>Targets the core, especially the rectus abdominis and obliques.</i></li>
</ul> </p>
    </div>
</div>


