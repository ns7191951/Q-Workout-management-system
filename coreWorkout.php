    <h3 class="card-title">Boat Pose</h3>
    <p class="card-text">"You'll definitely feel a minute of boat with the belly drawn in and the chest lifted," says West. It's tough to hold this for the full 60 seconds, but there's no better way to end a workout than with a challenge, right? (That being said, if you are struggling to maintain proper form for the full minute, take a break after 30 seconds, reset, and try holding for another 30.)<br>
  <ul>
 <li>Sit up straight with your legs bent, feet flat on the floor.</li>
  <li>Keeping your legs together, slowly lift them off the floor until they form a 45-degree angle to your torso. Engage your entire core, keep your back flat, and balance on your tailbone.</li>
  <li>You can keep your knees bent (as pictured) or straighten them out for more of a challenge.</li>
  <li>Reach your arms straight out in front of you, parallel to the floor. If you feel that you need some extra support, place your hands on the floor, underneath your hips.</li>
  <li>Hold here for 1 minute.</li>
  <li><i>Targets the core, hip flexors, and shoulders.</i></li>
</ul> </p>
