    <p class="card-text">"Shifting forward into a plank from Down Dog requires an immense amount of control and strength," says West.<br>
  <ul>
 <li>Start in a high plank with your wrists under your shoulders and your feet hip-width apart.</li>
  <li>Push your hips up and back to move into a Downward Dog with your heels reaching toward the floor.</li>
  <li>Keep your core tight and shift your weight forward to come back into a high plank.</li>
  <li>Continue for 1 minute.</li>
  <li><i>Targets the core, deltoids, and rhomboids.</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/core/dpushup.jpg" class="card-img-top text-center" style="width: 36rem;" alt="dpushup">
  <div class="card-body">
    <h3 class="card-title">Diamond Push-Up</h3>
    <p class="card-text">Diamond push-ups make the back of your arms seriously work, while the push-up position (basically a moving plank) still challenges your core, says West.<br>
  <ul>
 <li>Start in a high plank with your palms flat on the floor, hands shoulder-width apart, shoulders stacked directly above your wrists, legs extended behind you, and your core and glutes engaged.</li>
  <li>Walk your hands together so that your thumbs and forefingers form a triangle. This is the starting position.</li>
  <li>Bend your elbows to lower your chest toward the ground. Then, straighten your arms and push your body back up to the starting position. This is 1 rep.</li>
  <li>To make this easier, drop your knees to the ground. Just make sure to keep your core tight and your hips tucked in this position.</li>
  <li>Continue for 1 minute.</li>
  <li><i>Targets the core, triceps, deltoids, and pectorals.</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/core/plankwalk.jpg" class="card-img-top text-center" style="width: 36rem;" alt="plankwalk">
  <div class="card-body">
    <h3 class="card-title">Lateral Plank Walkp</h3>
    <p class="card-text">"The whole core is challenged in this one exercise to maintain stability and keep everything in motion," says West. "The arms are challenged first by the position and then the movement side to side."<br>
  <ul>
 <li>Start in a high plank position with your palms flat on the floor, hands shoulder-width apart, shoulders stacked directly above your wrists, legs extended behind you, and your core and glutes engaged.</li>
  <li>Take a step to the right starting with your right hand and right foot and following with your left hand and foot, maintaining a plank position as you move. This is 1 rep.</li>
  <li>Take a step to the right starting with your right hand and right foot and following with your left hand and foot, maintaining a plank position as you move. This is 1 rep.</li>
  <li>Do a set amount of reps in one direction, and then repeat the same amount moving in the opposite direction.</li>
  <li>Continue for 1 minute.</li>
  <li><i>Targets the core, deltoids, and triceps.</i></li>
</ul> </p>
    </div>
</div>

