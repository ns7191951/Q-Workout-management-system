    <h3 class="card-title">Seated Shoulder Squeeze</h3>
    <p class="card-text">"I love this stretch because it relieves poor posture and releases tension in the upper back," Jess Sims, a NASM-certified personal trainer and instructor at Shadowbox and the Fhitting Room in New York City, tells SELF.<br>
  <ul>
 <li>Sit on the floor with your knees bent and feet flat on the floor.</li>
  <li>Clasp your hands behind your lower back.</li>
  <li>Straighten and extend your arms and squeeze your shoulder blades together.</li>
  <li>Do this for 3 seconds, and then release. Repeat 5 to 10 times.</li>
   <li><i>Shoulder</i></li>
</ul> </p>
    </div>
