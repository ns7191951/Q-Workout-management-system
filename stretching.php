  <div class="card-body">
    <h3 class="card-title">Piriformis Stretch</h3>
    <p class="card-text">The piriformis muscle is a deep internal hip rotator, located on the outside of the butt. Its primary role is external rotation, Atkins says. "Deep internal rotators, while small, produce a lot of the movement at the hip and are often overlooked." Since the piriformis crosses over the sciatic nerve, "if it is tight, it can result in sciatic nerve irritation," Cyrelson says. "Stretching this muscle can prevent potential future sciatica, or help treat it."<br>
  <ul>
 <li>Sit on the floor with both legs extended in front of you.</li>
  <li>Cross your right leg over your left, and place your right foot flat on the floor. </li>
  <li>Place your right hand on the floor behind your body.</li>
  <li>Place your left hand on your right quad or your left elbow on your right knee (as shown) and press your right leg to the left as you twist your torso to the right.</li>
  <li>If the spinal rotation bothers your back, take it out and simply use your left hand to pull your right quad in and to the left.</li>
    <li><i>Stretches hips, back, glutes</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/stretching/lunge.jpg" class="card-img-top text-center" style="width: 36rem;" alt="lunge">
  <div class="card-body">
    <h3 class="card-title">Lunge With Spinal Twist</h3>
    <p class="card-text">Atkins notes that this stretch is commonly referred to as the World's Greatest Stretch (WGS) in the fitness community. And for good reason: "It's essential to help with posture-related pain or for people who sit for prolonged periods of time," says Dan Giordano, D.P.T., C.S.C.S., cofounder of Bespoke Treatments Physical Therapy in New York City and Seattle. "It helps open your hips and improve thoracic (mid-back) mobility," he tells SELF.<br>
  <ul>
 <li>Start standing with your feet together.</li>
  <li>Take a big step forward with your left foot, so that you are in a staggered stance.</li>
  <li>Bend your left knee and drop into a lunge, keeping your right leg straight behind you with your toes on the ground, so you feel a stretch at the front of your right thigh.</li>
  <li>Place your right hand on the floor and twist your upper body to the left as you extend your left arm toward the ceiling.</li>
  <li>Hold for 30 seconds to 2 minutes.</li>
   <li>Repeat on the other side.</li>
   <li><i>Stretches hip flexors, quads, back</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
