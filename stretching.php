    <h3 class="card-title"> Triceps Stretch</h3>
    <p class="card-text"><ul>
 <li>Kneel, sit, or stand tall with feet hip-width apart, arms extended overhead.</li>
  <li>Bend your right elbow and reach your right hand to touch the top middle of your back.</li>
  <li>Reach your left hand overhead and grasp just below your right elbow.</li>
  <li>Gently pull your right elbow down and toward your head.</li>
  <li>Switch arms and repeat.</li>
  <li><i>Stretches neck, shoulders, back, triceps</i></li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/stretching/9090.jpg" class="card-img-top text-center" style="width: 36rem;" alt="9090">
  <div class="card-body">
    <h3 class="card-title">90/90 Stretches</h3>
    <p class="card-text">This modification of pigeon pose helps with internal rotation of one leg and external rotation of the other, "so you're hitting both movements of the hip at once," Atkins says. It's a good option for people who have extremely tight hip flexors, she adds. "The front thigh is safely on the ground in a position that doesn't cause too much stress."<br>
  <ul>
 <li>Sit with your right knee bent at 90-degrees in front of you, calf perpendicular to your body and the sole of your foot facing to the left. Keep your right foot flexed.</li>
  <li>Let your leg rest flat on the floor.</li>
  <li>Place your left knee to the left of your body, and bend the knee so that your foot faces behind you. Keep your left foot flexed.</li>
  <li>Keep your right butt cheek on the floor. Try to move the left cheek as close to the floor as possible. It may not be possible if you're super tight.</li>
  <li>Hold for 30 seconds to 2 minutes.</li>
   <li>Repeat on the other side.</li>
   <li><i>Stretches hips</i></li>
</ul> </p>
    </div>
</div>


