<div class="card mx-5 mt-5">
  <img src="image/resistanceband/uprightrow.jpg" class="card-img-top text-center" style="width: 36rem;" alt="uprightrowbicepscurlpushupbandwalk">
  <div class="card-body">
    <h3 class="card-title"> Upright Row</h3><br>
    <p class="card-text"><b>Targets:</b> Shoulders
    	<br><br>
<b>How to do it:</b> <ul>
 <li>Stand with the band under your feet, shoulder-width apart. Shoulders should be back, spine straight and head facing forward (a).</li>
  <li>Hold the top of the band with a pronated (overhand) grip, hands close together and arms straight down in front of your body. This is the starting position (b).</li>
  <li>Lift your hands towards toward ceiling, raising them to about chin height, while keeping the hands close to the body. Your elbows should point to your sides and your forearms parallel to the floor (c).</li>
  <li>Return the bands back to the starting position (d). </li>
  <li> Repeat for 8-10 reps</li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/resistanceband/bicepscurl.jpg" class="card-img-top text-center" style="width: 36rem;" alt="bicepscurlpushupbandwalk">
  <div class="card-body">
    <h3 class="card-title"> Bicep Curl</h3><br>
    <p class="card-text"><b>Targets:</b> Biceps
    	<br><br>
<b>How to do it:</b> <ul>
 <li>Stand up tall with feet shoulder-width apart, band looped under your feet (a).</li>
  <li>Grab the top of the band using a supinated (underhand) grip with hands outside of hips and arms extended straight down. This is the starting position (b).</li>
  <li>Lift the band to about chin height with your arms bent into a curl and elbows pointing to the floor (c).</li>
  <li>Then, bring the band back down to the starting position with control. That’s one rep (d).</li>
  <li> Repeat for 8-10 reps</li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/resistanceband/pushup.jpg" class="card-img-top text-center" style="width: 36rem;" alt="pushupbandwalk">
  <div class="card-body">
    <h3 class="card-title">push up with band</h3><br>
    <p class="card-text"><b>Targets:</b> Chest, triceps, shoulders
    	<br><br>
<b>How to do it:</b> <ul>
 <li> Lay flat on your stomach with your legs straight behind you and toes tucked.</li>
  <li>Place the resistance band behind you so it’s lying across your mid-back. Then, hold the band with your hands so that each thumb is inside each end of the loop. Your hands shoulder-width apart, and arms are at your sides with your elbows bent (a).</li>
  <li>Do a push-up against the band to full extension, keeping your butt back in a straight line. (b).</li>
  <li>Then, slowly bring your body back down to the ground (c).</li>
  <li> Repeat for 8-10 reps</li>
</ul> </p>
    </div>
</div>

<div class="card mx-5 mt-5">
  <img src="image/resistanceband/bandwalk.jpg" class="card-img-top text-center" style="width: 36rem;" alt="bandwalk">
  <div class="card-body">
    <h3 class="card-title">Lateral Band Walk</h3><br>
    <p class="card-text"><b>Targets:</b> Hips, glutes, quadriceps, hamstrings
    	<br><br>
<b>How to do it:</b> <ul>
 <li> Place the resistance band around your ankles and get into a squat position with your thighs parallel to the ground and feet slightly wider than hip-distance apart (a).</li>
  <li>Step out to the left (laterally) against the band, remaining in the squat position with your hands in front you in an athletic stance (b). </li>
  <li> Alternate sides and step to your right against the band (c). Step out five times on each side (d). </li>
  <li>To increase the difficulty of this exercise and get a nice shoulder stretch, too, do the same exercise with your hands and arms extended overhead (e). </li>
  <li> Repeat for 8-10 reps</li>
</ul> </p>
    </div>
</div>


