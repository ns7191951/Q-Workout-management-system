  <div class="card-body">
    <h3 class="card-title">Squat to Press</h3><br>
    <p class="card-text"><b>Targets:</b> Full Body
    	<br><br>
<b>How to do it:</b> <ul>
 <li> Stand on top of the resistance band with feet hip-distance apart.</li>
  <li> Hold the handles with an overhand grip and your arms at shoulder-level, like you’re about to press them up overhead (a).</li>
  <li>  Drop into a squat so your knees are almost directly over your toes and thighs are parallel to the floor. Be sure to keep your hands by your shoulders (b).</li>
  <li>Slowly come up to standing, pressing the handles up overhead until your arms are fully extended (c).</li>
  <li>Bring your arms back to shoulder-height (d). </li>
  <li>  Repeat the exercise in one fluid motion: Squat, and then press up (e). Do 8-10 reps.</li>
</ul> </p>
    </div>
</div>

